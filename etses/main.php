         нэг бол түүнийг сонсоно, нэг бол өөрөө донгосно. Манай хүн сонсоод л , яриад л, инээгээд л, хоёулаа охидын зургийг харж элдвээр өөлж гоочлон, нэр хоч өгөн,
        эсвэл хөөрхөн сайхныг нь бахдан шагшаад л. Баадай маань байсхийгээд л хэн нэг охинд хайртай дуртай болчихно.<br><br>
